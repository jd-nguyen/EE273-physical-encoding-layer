// Assumes the master gm scrambler. Resets to 1
// Assumes Tx_mode is for data when TX_EN is high, or data is in the send FIFO,
// ESD and CSReset sent by state machine at end of packet.
// Otherwide tx_mode is sending idles.
// Output coding: 2s complement, not sign magnitude
//   000= 0
//   001=+1
//   010=+2
//   111=-1
//   110=-2
//

module DUTS26_0(input logic Clk,input logic Reset,
                input logic [7:0] Din,input logic TX_EN,
                output logic [3:0][2:0] Dout);
`protect
reg [32:0] Scr,Scr_d;

reg [3:0] Sx,Sy,Synm1,Sg;
reg Xn,Yn;
reg [4:0] tx_enable,tx_enable_d;
reg [7:0] Sc;
logic data_to_send;
reg nr_flag;
reg [2:0] CS,CSnm1,CSnm1_d;
reg [8:0] Sd,Sd_d;
reg [11:0] dout,dout_d;
reg [3:0] cresetn;
reg csreset;
reg OE,OE_d;
typedef enum reg[3:0] {
    s_reset,
    s_send_idle,
    s_SDD1,
    s_SDD2,
    s_Transmit_data,
    s_CSR1,
    s_CSR2,
    s_ESD1,
    s_ESD2
} STATE;

typedef struct packed {
    reg signed [2:0] A,B,C,D;
} q54;

reg [7:0][7:0] tx_fifo,tx_fifo_d;
reg [3:0] rp,rp_d,wp,wp_d;
reg [4:0] depth,depth_d;
q54 postLookup;
q54 finalLookup;
const reg [11:0] SDD1=12'b010_010_010_010;
const reg [11:0] SDD2=12'b010_010_010_110;
const reg [11:0] ESD1=12'b010_010_010_010;
const reg [11:0] ESD2=12'b010_010_010_110;
reg [7:0] TXD;
logic srev;

STATE cstate,nstate;

function reg [32:0] scrambler_master(reg [32:0] scrin);
    reg [32:0] wk;
    wk= {scrin[31:0],scrin[32]^scrin[12]};
    return wk;
endfunction : scrambler_master

always @(*) begin
    Scr_d=scrambler_master(Scr);
    nstate=cstate;
    rp_d=rp;
    wp_d=wp;
    depth_d=depth;
    dout_d=dout;
    Dout=dout;
    cresetn=tx_enable[2]&(~tx_enable[0]);
    tx_fifo_d=tx_fifo;
    rp_d=rp;
    wp_d=wp;
    depth_d=depth;
    OE_d=~OE;
    TXD=Din;
    csreset=0;
    srev=tx_enable[4]+tx_enable[2];
    case(cstate)
        s_reset : begin
            nstate=s_send_idle;
            postLookup=lookup_idle(Sd);
        end
        s_send_idle : begin
            if(TX_EN) begin
                nstate=s_SDD2;
                postLookup=SDD1;
            end else begin
                postLookup=lookup_idle(Sd);
            end
        end
        s_SDD1 : begin
            nstate=s_SDD2;
            postLookup=SDD1;
        end
        s_SDD2 : begin
            nstate=s_Transmit_data;
            postLookup=SDD2;
        end
        s_Transmit_data : begin
            if(TX_EN) begin
                postLookup=lookup12(Sd);
            end else begin
                postLookup=lookup_creset(Sd);
                csreset=1;
                nstate=s_CSR2;
            end
        end
        s_CSR2 : begin
            postLookup=lookup_creset(Sd);
            csreset=1;
            nstate=s_ESD1;
        end
        s_ESD1 : begin
            postLookup=ESD1;
            nstate=s_ESD2;
        end
        s_ESD2 : begin
            postLookup=ESD2;
            nstate=s_send_idle;
        end

    endcase
    case({wp!=wp_d,rp!=rp_d})
        2'b00 : depth_d=depth;
        2'b11 : depth_d=depth;
        2'b10 : depth_d=depth+1;
        2'b01 : depth_d=depth-1;
    endcase

    tx_enable_d=(tx_enable<<1)|TX_EN;

    Xn=Scr[4]^Scr[6];
    Yn=Scr[1]^Scr[5];
    Sy[0]=Scr[0];
    Sy[1]=Scr[3]^Scr[8];
    Sy[2]=Scr[6]^Scr[16];
    Sy[3]=Scr[9]^Scr[14]^Scr[19]^Scr[24];

    Sx[0]=Scr[4]^Scr[6];
    Sx[1]=Scr[7] ^ Scr[9] ^ Scr[12] ^ Scr[14];
    Sx[2] = Scr[10] ^ Scr[12] ^ Scr[20] ^ Scr[22];
    Sx[3] = Scr[13] ^ Scr[15] ^ Scr[18] ^ Scr[20] ^ Scr[23] ^ Scr[25] ^
            Scr[28] ^ Scr[30];

    Sg[0] = Scr[1] ^ Scr[5];
    Sg[1] = Scr[4] ^ Scr[8] ^ Scr[9] ^ Scr[13];
    Sg[2] = Scr[7] ^ Scr[11] ^ Scr[17] ^ Scr[21];
    Sg[3] = Scr[10] ^ Scr[14] ^ Scr[15] ^ Scr[19] ^ Scr[20] ^ Scr[24] ^
            Scr[25] ^ Scr[29];

    Sc[7:4] = (tx_enable[2])?Sx:4'h0;
    Sc[3:1] = (!OE)?Sy[3:1]:~Synm1[3:1];
    Sc[0] = Sy[0];
    CS[2]=(tx_enable[2] == 1)?Sd[7]^TXD[7]:0;
    CS[1]=(tx_enable[2] == 1)?Sd[6]^CSnm1[1]:0;
    CS[0]=CSnm1[2];
    CSnm1_d=CS;

    Sd[8]=CS[0];
    Sd[7]=(csreset==0 & tx_enable[2])?Sc[7]^TXD[7]:
        ((csreset)?CSnm1[1]:Sc[7]);
    Sd[6]=(csreset==0 & tx_enable[2])?Sc[6]^TXD[6]:
        ((csreset)?CSnm1[1]:Sc[6]);
    Sd[5:4]=(tx_enable[2])?Sc[5:4]^TXD[5:4]:Sc[5:4];
    Sd[3]=(tx_enable[2])?Sc[3]^TXD[3]:Sc[3]; // loc_lpi_req is always false
    Sd[2]=(tx_enable[2])?Sc[2]^TXD[2]:Sc[2];
    Sd[1]=(tx_enable[2])?Sc[1]^TXD[1]:Sc[1]^1;
    Sd[0]=(Sc[0]^tx_enable[2])?TXD[0]:0;
    finalLookup=postLookup;
    if( Sg[0]^srev ) finalLookup.A=-postLookup.A;
    if( Sg[1]^srev ) finalLookup.B=-postLookup.B;
    if( Sg[2]^srev ) finalLookup.C=-postLookup.C;
    if( Sg[3]^srev ) finalLookup.D=-postLookup.D;
    dout_d=finalLookup;


end

always @(posedge(Clk) or posedge(Reset)) begin
    if(Reset) begin
        Scr <= 1;
        Synm1 <= 0;
        nr_flag <= 1;
        CSnm1 <= 0;
        cstate <= s_reset;
        dout <= 0;
        tx_fifo <= 0;
        rp <=0;
        wp<=0;
        depth<=0;
        tx_enable <= 0;
        OE<=1;
    end else begin
        Scr <= #1 Scr_d;
        Synm1 <= #1 Sy;
        nr_flag <= #1 ~nr_flag;
        CSnm1 <= #1 CSnm1_d;
        cstate <= #1 nstate;
        dout <= #1 dout_d;
        tx_fifo <= #1 tx_fifo_d;
        rp <= #1 rp_d;
        wp <= #1 wp_d;
        depth <= #1 depth_d;
        tx_enable <= #1 tx_enable_d;
        OE<= #1 OE_d;
    end
end

